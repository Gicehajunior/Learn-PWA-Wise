            <br><br>
            Since a progressive web app is a type of webpage or website known as a web application, they do not require separate bundling or distribution. 
            In particular, there is no requirement for developers or users to install the web apps via digital distribution systems like Apple App Store or Google Play. 
            However, for many PWA providers the lack of visibility of the PWA in those distribution systems is a disadvantage compared to native apps. To varying degrees, 
            the major app stores started to embrace PWAs, allowing them to be found in app stores.

            <br><br>
            As of April 2018, the PWA features are supported to varying degrees by the Mozilla Firefox, 
            Google Chrome, Apple Safari, and Microsoft Edge browsers, but more browsers may support the features needed in the future.
            Several businesses highlight significant improvements in a wide variety of key performance indicators after PWA implementation, 
            like increased time spent on page, conversions, or revenue.
